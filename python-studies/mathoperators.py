# math operators
n1 = float(input('Digite um numero'))
n2 = float(input('Digite um outro numero'))
a = n1 + n2
b = n1 - n2
c = n1 * n2
d = n1 / n2
di = n1 // n2
f = n1 ** n2
print('A soma é {}, a subtraçao é {}, o produto é {}, a divisão é {}, a divisão inteira {}'.format(a,b,c,d,di))
print ('A potencia é {}' .format(f))

